package com.example.login;

public class PDFUpload {

    public String name;
    public String url;

    public PDFUpload() {
    }

    public PDFUpload(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }



    public String getUrl() {
        return url;
    }


}
